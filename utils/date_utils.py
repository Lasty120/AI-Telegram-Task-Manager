"""
Утилиты для работы с датами задач.

Центральное место для константы «задача без срока» (FALLBACK_TASK_TIMESTAMP).
Все модули проекта должны импортировать её отсюда, а не объявлять локально —
это соответствует принципу DRY и позволяет изменить значение в одном месте.
"""

from datetime import datetime, timezone

# ─── Константа: метка «нет срока» ────────────────────────────────────────────
# Используется для задач, у которых пользователь не указал конкретную дату.
# Значение 01.01.2060 00:00:00 UTC выбрано как условно «бесконечность»:
#   • далеко в будущем → планировщик никогда не отправит лишнее напоминание;
#   • явно отличается от любой реальной даты → легко обнаружить и отфильтровать.
FALLBACK_TASK_TIMESTAMP: int = int(datetime(2060, 1, 1, 0, 0, 0, tzinfo=timezone.utc).timestamp())


def is_fallback_timestamp(ts: int | None) -> bool:
    """
    Возвращает True, если переданный Unix-timestamp является меткой «нет срока».

    Используется перед отправкой данных в Notion, чтобы не записывать
    искусственную дату 01.01.2060 в поле date страницы.

    Args:
        ts: Unix-timestamp для проверки (или None).

    Returns:
        True, если значение равно FALLBACK_TASK_TIMESTAMP или None.
    """
    if ts is None:
        return True
    return ts == FALLBACK_TASK_TIMESTAMP
